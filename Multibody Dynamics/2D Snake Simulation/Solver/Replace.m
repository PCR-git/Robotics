
function [Tnew,Ynew,Tenew,Yenew,PHI_Tnew,qppnew] = Replace(Told,Yold,Teold,Yeold,PHI_Told,qppold)

% Tnew = Told;
% Ynew = Yold;
% Tenew = Teold;
% Yenew = Yeold;
% PHI_Tnew = PHI_Told;
% qppnew = qppold;

Tnew = 0*Told;
Ynew = 0*Yold;
Tenew = 0*Teold;
Yenew = 0*Yeold;
PHI_Tnew = 0*PHI_Told;
qppnew = 0*qppold;

end